     public void autoID()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/warehouse","root","");
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("select Max(id) from job");
            rs.next();
            
            rs.getString("Max(id)");
            
            
            if(rs.getString("Max(id)")==null)
            {
                
                txtno.setText("E-1");
            }
            else
            {
                Long id = Long.parseLong(rs.getString("Max(id)").substring(2,rs.getString("Max(id)").length()));
                id++;
                txtno.setText("E-" + String.format("%d", id));
                
                
                
                
            }
         
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
