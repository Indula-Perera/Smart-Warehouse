    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if(txtname.getText().trim().isEmpty() && txtmail.getText().trim().isEmpty() && txtnic.getText().trim().isEmpty() && txtphone.getText().trim().isEmpty() && txtadd.getText().trim().isEmpty() ){
        lbl_name.setText("Name Required.");
        lbl_mail.setText("E-Mail Required.");
        lbl_nic.setText("NIC Required.");
        lbl_phone.setText("Phone No. Required.");
        lbl_add.setText("Address Required.");
        }
        else if(txtname.getText().trim().isEmpty()){
             lbl_name.setText("Name Required.");
        }
        else if(txtmail.getText().trim().isEmpty()){
            lbl_mail.setText("E-Mail Required.");
        }
        else if(txtnic.getText().trim().isEmpty()){
            lbl_nic.setText("NIC Required.");
        }
        else if(txtphone.getText().trim().isEmpty()){
           lbl_phone.setText("Phone No. Required.");
        }
        else if(txtadd.getText().trim().isEmpty()){
             lbl_add.setText("Address Required.");
        }
        else{
        try {
            // TODO add your handling code here:

            
            String no = txtno.getText();
            String name = txtname.getText();
            String email = txtmail.getText();
            String nic = txtnic.getText();
            String phone = txtphone.getText();
            String address = txtadd.getText();
            txtname.requestFocus();
            
            
            
            
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/warehouse","root","");
            PreparedStatement pst = con.prepareStatement("insert into reg (id,name,email,nic,phone,address)values(?,?,?,?,?,?)");
            pst.setString(1, no);
            pst.setString(2, name);
            pst.setString(3, email);
            pst.setString(4, nic);
            pst.setString(5, phone);
            pst.setString(6, address);
            
            pst.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this,"User Registered Successfully.");
            
              autoID();
              
              txtname.setText("");
              txtmail.setText("");
              txtnic.setText("");
              txtphone.setText("");
              txtadd.setText("");
              txtname.requestFocus(); 
              
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(autoIn.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

