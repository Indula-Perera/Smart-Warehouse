    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
                String str = no.getText();
                //Database Connection
                Class.forName("com.mysql.jdbc.Driver");  
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/warehouse","root","");
                PreparedStatement st = con.prepareStatement("select * from job where id=?");
                st.setString(1, str);
                //Excuting Query
                ResultSet rs = st.executeQuery();
                if (rs.next()) {
                    String s = rs.getString(1);
                    String s1 = rs.getString(2);
                    String s2 = rs.getString(3);
                    String s3 = rs.getString(4);
                    String s4 = rs.getString(5);
                    //Sets Records in TextFields.
                    no.setText(s);
                    catergory.setText(s1);
                    duration.setText(s2);
                    space.setText(s3);
                    quantity.setText(s4);
                    no.requestFocus();
                } else {
                 
                 catergory.setText("");
                 duration.setText("");
                 space.setText("");
                 quantity.setText("");
                 
                }
                //Create Exception Handler
            } catch (HeadlessException | SQLException ex) {
                System.out.println(ex);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(calculate_bill_of_storage_job.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

