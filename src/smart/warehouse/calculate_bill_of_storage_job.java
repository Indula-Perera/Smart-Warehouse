                // TODO add your handling code here:
                String id = no.getText();
                String catergor = catergory.getText();
                String duratio = duration1.getText();
                String spac = space11.getText();
                String quantit = quantity2.getText();
                String tota = total.getText();
                
                //Database Connection
                Class.forName("com.mysql.jdbc.Driver");  
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/warehouse","root","");
