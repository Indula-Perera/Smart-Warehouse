    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
       String id = (no.getText());
            String cater = catergory.getText();
            int dura = Integer.parseInt(duration.getText());
            int spac = Integer.parseInt(space.getText());
            int quan = Integer.parseInt(quantity2.getText());
            String tot = total.getText();

        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            
            model.addRow(new Object[]
                    {
    
                        id ,
                        cater,
                        dura,
                        spac,
                        quan,
                        tot,
                    });
            
             int sum = 0;
            
             for(int i = 0; i<jTable1.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 5).toString());
        }
        
        total1.setText(Integer.toString(sum));
        
        no.setText("");
        catergory.setText("");
        duration.setText("");
        space.setText("");
        quantity2.setText("");
        total.setText("");
        no.requestFocus();
    }//GEN-LAST:event_jButton4ActionPerformed

