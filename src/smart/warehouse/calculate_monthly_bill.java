    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          if(no.getText().trim().isEmpty() ){
        lbl_no.setText("Required Emp no");
        
        }
        else if(no.getText().trim().isEmpty()){
            lbl_no.setText("Required Emp no");
        }
        
        else{
         try {
            //Database Connection
           Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/warehouse","root","");
            String id = no.getText();
            PreparedStatement pst = con.prepareStatement("select id,catergory,duration,space,quantity,total from bill where id = ?");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            if(rs.next()==true)
            {
                String cater = rs.getString(2);
                String dura = rs.getString(3);
                String spac = rs.getString(4);
                String quan = rs.getString(5);
                String tot = rs.getString(6);
                catergory.setText(cater);
                duration.setText(dura);
                space.setText(spac);
                quantity2.setText(quan);
                total.setText(tot);
            }   
            else
            {
                 catergory.setText("");
                 duration.setText("");
                 space.setText("");
                 total.setText("");
                 total.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(find.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(calculate_monthly_bill.class.getName()).log(Level.SEVERE, null, ex);
        }}  
    }//GEN-LAST:event_jButton2ActionPerformed

